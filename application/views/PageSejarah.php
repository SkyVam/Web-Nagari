                        <p style="text-align: justify;">Setelah beberapa lama bertempat tinggal dengan mendirikan pondok pondok pemungkiman dan menggarap areal daerah Parik Panjang , anak kemanakan semangkin banyak dan peradapan semangkin berkembang maka dibangunlah sebuah nagari karena dilihat syaratnya sudah cukup Yaitu :<br>
