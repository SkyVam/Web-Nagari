                        <p style="text-align: justify;">Asal-usul Nagari Parik Panjang berdasarkan suatu tulisan / cerita dari peristiwa yang terjadi pada masa dahulu, cerita dari mulut kemulut diperdapat dari orang Tua-tua masyarakat Nagari Parik Panjang. Nenek Moyang Masyarakat Nagari Parik Panjang pada mulanya beberapa kelompok orang yang terdiri dari beberapa Suku datang kedaerah ini. Kelompok pertama datang dari daerah Batu Sangkar ( Paga Ruyuang ) melalui Sariak Sungai Pua terus Kekoto Gadang, Sianok Sungai Jariang kemudian ke Kampung Pisang, terus ke Panta, selanjutnya ke Batu  Sigulambai dan akhirnya menyeberangi Batang Matua terus mendaki ke Laman Gadang, dan sampailah Iko Tanah yang sekarang ini terletak di Jorong Batu Baselo dan kelompok orang ini terdiri dari Suku Sikumbang, kelompok kedua datang juga dari Batu Sangkar ( Paga ruyung ) melalui Padang Kunyit, Tilatang Kamang terus ke Batuang Babuai, Sitingkai dan berhenti di Aia Taganang / Lurah Taganang yang sekarang ini kita sebut Jorong Lurah Taganang dan kelompok ini juga terdiri dari beberapa suku antara lain Suku Caniago dan Suku Tanjuang.</p>
                        <p style="text-align: justify;">Perjalanan kelompok orang – orang tersebut tidaklah berjalan terus menerus sampai kedaerah ini, tetapi memakan selang waktu yang cukup lama, dan akhirnya sampailah mereka didaerah Matua ini, lalu berhenti dan membuat pemondokan, sebab daerah inilah yang mereka anggap baik untuknya pengembangan. Di daerah Matua inilah mereka semangkin berkembang sehingga orang yang dewasa pada waktu itu mencapai jumlah 27 orang yang terdiri dari Niniak mamak – Niniak mamak.</p>
                        <p style="text-align: justify;">Kelompok tersebut itulah maka diantara Niniak mamak yang 27 orang ini diangkatlah seorang Ketua yang sudah berpengalaman dan usianya lebih tua dari pada mereka semua, setelah diamati dan dilihat areal daerah ini cukup luas dan sangat baik untuk dikembangkan. Selanjutnya diadakanlah musyawarah dan mufakat maka diambilah suatu keputusan oleh Ketua rombongan untuk membagi areal daerah itu kepada beberapa kelompok orang dalam rombongan tersebut dengan cara sebagai berikut : <br><br>
1.      Angku-angku sanak saudara nan sapuluah urang ko, pailah ka …udik-udik tuu..! sehingga disebut Matua mudik ( karena telah diatur sepuluh orang ini kemudiak ) yang terdiri dari tiga suku yakni : Caniago, Tanjuang, dan Sikumbang. <br><br>
2.      Dan angku-angku sanak saudara nan sapuluah urang lai ko, tamasuak ambo pailah ka hilia-hilia tu… sehingga disebut Matua Hilia ( karena telah diatur yang sepuluh orang ini ke hilir ) yang terdiri juga dari tiga suku tersebut diatas yaitu Suku Sikumbang, Tanjuang Caniago. <br><br>
3.      Akhirnya Angku-angku nan tingga tujuah urang ko, pailah ka… Parik nan Panjang tu. Maksudnya Parik nan Panjang tu bukit memanjang dari arah Batu baselo tadi kearah timur dan orang –orang tersebut terdiri juga dari tiga suku yaitu : Suku Sikumbang, Tanjuang, Caniago.</p>
